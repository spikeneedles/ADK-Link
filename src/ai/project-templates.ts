// ==================== TEMPLATE REGISTRY ====================
export interface Template {
  id: string;
  name: string;
  category: string;
  description: string;
  files: Record<string, string>;
}

export const ALL_TEMPLATES: Template[] = [
  // Frontend
  NEXTJS_TEMPLATE,
  REACT_VITE_TEMPLATE,
  VUE_TEMPLATE,
  SVELTE_TEMPLATE,
  HTML_TAILWIND_TEMPLATE,
  ASTRO_TEMPLATE,
  
  // Backend
  EXPRESS_TEMPLATE,
  PYTHON_CLI_TEMPLATE,
  RUST_CLI_TEMPLATE,
  GO_CLI_TEMPLATE,
  FASTAPI_TEMPLATE,
  NESTJS_TEMPLATE,
  DJANGO_TEMPLATE,
  
  // Mobile
  FLUTTER_TEMPLATE,
  REACT_NATIVE_TEMPLATE,
  ANDROID_KOTLIN_TEMPLATE,
];

/**
 * Detect which template to use based on user prompt
 */
export function detectTemplate(userPrompt: string): Template | null {
  const lower = userPrompt.toLowerCase();
  
  // Next.js detection
  if (lower.includes('nextjs') || lower.includes('next.js') || lower.includes('next app')) {
    return NEXTJS_TEMPLATE;
  }
  
  // Express API detection
  if ((lower.includes('express') || lower.includes('rest api') || lower.includes('api server')) 
      && (lower.includes('node') || lower.includes('typescript'))) {
    return EXPRESS_TEMPLATE;
  }
  
  // Python CLI detection
  if (lower.includes('python') && (lower.includes('cli') || lower.includes('command line') || lower.includes('tool'))) {
    return PYTHON_CLI_TEMPLATE;
  }
  
  // Rust CLI detection
  if (lower.includes('rust') && (lower.includes('cli') || lower.includes('command line') || lower.includes('tool'))) {
    return RUST_CLI_TEMPLATE;
  }
  
  return null;
}

/**
 * Get template by ID
 */
export function getTemplateById(id: string): Template | undefined {
  return ALL_TEMPLATES.find(t => t.id === id);
}

/**
 * Find templates matching a query
 */
export function findTemplates(query: string): Template[] {
  const lowerQuery = query.toLowerCase();
  return ALL_TEMPLATES.filter(t => 
    t.name.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery)
  );
}
